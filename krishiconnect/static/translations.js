function changeLanguage(lang) {
    console.log("Language changed to:", lang);
    if (!translations[lang]) return;
    localStorage.setItem('selectedLanguage', lang);
    
    
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Handle title attributes (tooltips)
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        if (translations[lang][key]) {
            element.title = translations[lang][key];
        }
    });

    // Handle value attributes (for input buttons like submit)
    document.querySelectorAll('[data-i18n-value]').forEach(element => {
        const key = element.getAttribute('data-i18n-value');
        if (translations[lang][key]) {
            element.value = translations[lang][key];
        }
    });
    
    const selector = document.getElementById('language-selector');
    if (selector) selector.value = lang;
}

function getTranslation(key) {
    const lang = localStorage.getItem('selectedLanguage') || 'en';
    return (translations[lang] && translations[lang][key]) ? translations[lang][key] : key;
}

function detectBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage || 'en';
    if (browserLang.startsWith('hi')) return 'hi';
    if (browserLang.startsWith('mr')) return 'mr';
    return 'en';
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage');
    const lang = savedLang || detectBrowserLanguage();
    changeLanguage(lang);

    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }

    // Profile Form Validation for City Field
    const cityInputs = document.querySelectorAll('input[name="city"]');
    cityInputs.forEach(input => {
        const form = input.closest('form');
        // Ensure it's a profile form by checking for other common profile fields like phone or address
        if (form && (form.querySelector('input[name="phone"]') || form.querySelector('[name="address"]'))) {
            form.addEventListener('submit', function(e) {
                if (input.value.trim() === '') {
                    e.preventDefault();
                    const currentLang = localStorage.getItem('selectedLanguage') || 'en';
                    const msg = (translations[currentLang] && translations[currentLang]['fill_all_fields']) ? translations[currentLang]['fill_all_fields'] : "Please fill all fields";
                    alert(msg);
                    input.focus();
                }
            });
        }
    });
});